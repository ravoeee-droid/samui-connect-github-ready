const LanguageContext = createContext(null);

function detectLanguage() {
  if (typeof window === 'undefined') return 'en';
  const stored = window.localStorage.getItem('samui_language');
  if (stored && dictionaries[stored]) return stored;
  const browser = window.navigator.language?.slice(0, 2);
  return dictionaries[browser] ? browser : 'en';
}

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(detectLanguage);

  useEffect(() => {
    document.documentElement.lang = language;
    window.localStorage.setItem('samui_language', language);
  }, [language]);

  const value = useMemo(() => {
    const dictionary = dictionaries[language] || dictionaries.en;
    const t = (key, replacements = {}) => {
      const fallback = dictionaries.en[key] || key;
      let text = dictionary[key] || fallback;
      Object.entries(replacements).forEach(([name, value]) => {
        text = text.replaceAll(`{${name}}`, value ?? '');
      });
      return text;
    };

    return {
      language,
      setLanguage: (next) => dictionaries[next] && setLanguageState(next),
      currentLanguage: SUPPORTED_LANGUAGES.find((item) => item.code === language) || SUPPORTED_LANGUAGES[0],
      languages: SUPPORTED_LANGUAGES,
      t,
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) throw new Error('useLanguage must be used inside LanguageProvider');
  return context;
}

export function buildLocalizedRentalMessage(t, listing, request) {
  const lines = [
    t('whatsapp.intro', { title: listing.title }),
    `${t('whatsapp.location')}: ${listing.location}`,
    `${t('whatsapp.price')}: ${listing.price}`,
    request.date ? `${t('whatsapp.date')}: ${request.date}` : null,
    request.duration ? `${t('whatsapp.duration')}: ${request.duration}` : null,
    request.name ? `${t('whatsapp.name')}: ${request.name}` : null,
    request.note ? `${t('whatsapp.message')}: ${request.note}` : null,
  ].filter(Boolean);

  return encodeURIComponent(lines.join('\n'));
}
